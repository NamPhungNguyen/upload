package Model;

/**
 *
 * @author Admin
 */
public class Funiture {
    private String name;
    private Boolean status;
    private int price;

    public Funiture() {
    }

    public Funiture(String name, Boolean status, int price) {
        this.name = name;
        this.status = status;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
}
